public class Loops {
    public static void main(String[] args) {
        // while loop 
        // intitilization 
        // check condition 
        // improve ur block 
        // inc / dec 
        int i=1 ;  // 1. intitilization 
        while(i<=5) // 2. check condition 
        {
            System.out.println(i); //3. improve ur block
            i++; // inc / dec 
        }

        // do while 
        int j=100;
        do 
        {
            System.out.println(j); //3. improve ur block
            j++;
 
        } while(j<=105);
         // for loop 
         for (int a=0;a<=5;a++) // init;condition;inc 
         {
            System.out.println(a);
         }

         // for each 

         // Array 
         String[] names = {"srini","swapna","sowjanya","radha"};
        
         for (String name : names)
         {
           System.out.println(name);
         }

}
}
