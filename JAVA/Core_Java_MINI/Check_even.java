public class Check_even {
    public static void main(String[] args) {
        // for loop 
        for (int i=0; i<20;i++)
        {
            // condition 
            if (i%2==0)
            {
                System.out.println(i+" is even number ");
            }
        }
        // 5 multiples 
        for (int i=0; i<20;i++)
        {
            // condition 
            if (i%5==0)
            {
                System.out.println(i+" is 5 multiple");
            }

        }

    }
}
